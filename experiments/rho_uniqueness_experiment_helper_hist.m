function [ax] = rho_uniqueness_experiment_helper_hist(out_struct_max, title_str)
gap_struct = out_struct_max.constrained_certified_suboptimality_gap;
lagrangian_bounds = cellfun(@(x) x.lagrangian_bound, gap_struct);
lagrangian_bounds_discrete = cellfun(@(x) x.lagrangian_bound_discrete, gap_struct);
xmin_high_bounds = cellfun(@(x) x.xmin_high_bound, gap_struct);

ax = axes;
edges = 0:2e-5:2e-4;
if max(xmin_high_bounds) < 1e-4 && max(lagrangian_bounds_discrete) < 1e-4
    edges = edges / 2;
end
histogram(ax, xmin_high_bounds, edges); hold on; 
histogram(ax, lagrangian_bounds_discrete, edges);

set(ax,...
    'Units','normalized',...
    'Position',[.2 .22 .8 .77],...
    'XTick',[0 1 2]*1e-4,...
    'XLim',[0 2e-4],...
    'XTickLabels',{'$0$','','$2 \times 10^{-4}$'},...
    'TickLabelInterpreter','latex',...
    'FontUnits','points',...
    'FontWeight','normal',...
    'FontSize',10,...
    'FontName','Times');

% xlabel(ax, 'Sorted order index percentile',...
%     'FontUnits','points',...
%     'interpreter','latex',...
%     'FontSize',10,...
%     'FontName','Times');
% ylabel(ax, 'Value',...
%     'FontUnits','points',...
%     'interpreter','latex',...
%     'FontSize',10,...
%     'FontName','Times');
% legend({'Sorted $\rho$ values', 'Threshold $\lambda^*$'},...
%     'FontUnits','points',...
%     'interpreter','latex',...
%     'FontSize',6,...
%     'FontName','Times');


title(ax, title_str,...
    'FontUnits','points',...
    'FontWeight','normal',...
    'FontSize',10,...
    'FontName','Times');


end